public class PrimitiveVariablesExample {
    public static void main(String[] args) {

        int a = 100;
        double b = 10.5;
        char c = 'A';
        boolean isJavaFun = true;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(isJavaFun);
    }
}